#CRIAÇÃO DE LISTA COM RANGE()
# O RANGE() DEFINI UM INTERVALOS DE VALORES INTEIROS.
# PODE TER 3 PARAMETROS:
#   range(n)        intervalo de 0 an-1
#   range(i, n)     intervalo de i a n_1
#   range(i,n,p)    intervalo de i a n-1 de p em p.

L1 = list(range(6))
print("lista L1: ",L1)
L2 = list(range(10, 16))
print("lista L2: ",L2)
L3 = list(range(2,21,2))
print("lista L3: ",L3)