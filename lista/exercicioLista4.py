
# Dada a lista L = [2, 6, 5, 8, 3, 0, 9], escreva um programa em PYTHON que
# imprima as seguintes informações. (1,0 ponto)
# a)  tamanho da lista.
#
# b)  maior valor da lista.
#
# c)  menor valor da lista.
#
# d)  soma de todos os elementos da lista.

L = [2, 6, 5, 8, 3, 0, 9]

print("tamanho da lista: ",len(L))
print("maior valor da lista: ",max(L))
print("menor valor da lista: ",min(L))
print("soma de todos os valores da lista: ",sum(L))


